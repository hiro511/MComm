package simulatepath;

public class MicroTubule {
	Position plusEndCentre;
	Position minusEndCentre;
	double radiusMicroTubule;
	
	public Position getPlusEndCentre() {
		return plusEndCentre;
	}
	public void setPlusEndCentre(Position plusEndCentre) {
		this.plusEndCentre = plusEndCentre;
	}
	public Position getMinusEndCentre() {
		return minusEndCentre;
	}
	public void setMinusEndCentre(Position minusEndCentre) {
		this.minusEndCentre = minusEndCentre;
	}
	public double getRadiusMicroTubule() {
		return radiusMicroTubule;
	}
	public void setRadiusMicroTubule(double radiusMicroTubule) {
		this.radiusMicroTubule = radiusMicroTubule;
	}
}
